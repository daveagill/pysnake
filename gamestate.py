from sml import vector
import snake
import grid


class GameState(object):
    def __init__(self, width = 32, height = 24, level = 1):
        self.width = width
        self.height = height
        self.level = level # current unused
        self.arena = grid.Grid(width, GameState._loadArena(level))
        self.reset()
    
    def reset(self):
        self.snake = self._initialSnake()
        self.randomiseFood()
        self.alive = True
    
    def bodyCollision(self):
        return self.snake.selfCollision()
        
    def wallCollision(self):
        return self.arena.get(self.snake.head.position.x, self.snake.head.position.y) == 1
    
    def foodCollision(self):
        return self.snake.head.position == self.foodPosition
    
    def randomiseFood(self):
        def _randcell():
            return vector.randomRange(0, self.width, 0, self.height)
        
        self.foodPosition = _randcell()
        while not self._emptyCell(self.foodPosition):
            self.foodPosition = _randcell()
        
    def _initialSnake(self):
        return snake.Snake(self.width // 2, self.height // 2, 5, snake.UP)
    
    def _emptyCell(self, position):
        if self.arena.get(position.x, position.y) == 0:
            for segment in self.snake.body:
                if segment.position == position:
                    return False
            return True
        return False

